function mapper(digits) {
    let strings = [];

    digits.map((digit) => {
        switch (digit) {
            case digit == 2:
                strings = [a, b, c];
            case digit == 3:
                strings = [d,e,f];
            case digit == 4:
                strings = [g, h, i];
            case digit == 5:
                strings = [j, k, l];
            case digit == 6:
                strings = [m, n, o];
            case digit == 7:
                strings = [p, q, r, s];
            case digit == 8:
                strings = [t, u, v];
            case digit == 9:
                strings = [w, x, y, z];

        }
    })
}